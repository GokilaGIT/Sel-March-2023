package week1.day1;

public class LearnIf {

	public static void main(String[] args) {
		int number = 0;
		if(number>0) {
			System.out.println("The given "+ number + "is Positive");
		}
		else if(number==0) {
			System.out.println("The number is neutral");
		}
		else {
			System.out.println("The given "+number + "is negative");
		}

	}

}
